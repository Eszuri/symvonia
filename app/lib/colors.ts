export type AccentColor = 'green' | 'blue' | 'purple' | 'pink' | 'red' | 'orange' | 'yellow' | 'teal' | 'cyan' | 'indigo' | 'rose' | 'lime' | 'amber' | 'emerald' | 'custom';

function hexToRgb(hex: string) {
  const clean = hex.replace('#', '');
  const r = parseInt(clean.slice(0, 2), 16);
  const g = parseInt(clean.slice(2, 4), 16);
  const b = parseInt(clean.slice(4, 6), 16);
  return { r, g, b };
}

export function setCustomAccentVars(hex: string) {
  if (typeof document === 'undefined') return;
  const { r, g, b } = hexToRgb(hex);
  const root = document.documentElement;
  root.style.setProperty('--accent-500', hex);
  root.style.setProperty('--accent-400', `rgb(${Math.min(255, r + 40)}, ${Math.min(255, g + 40)}, ${Math.min(255, b + 40)})`);
  root.style.setProperty('--accent-600', `rgb(${Math.max(0, r - 30)}, ${Math.max(0, g - 30)}, ${Math.max(0, b - 30)})`);
  root.style.setProperty('--accent-500-10', `rgba(${r}, ${g}, ${b}, 0.1)`);
  root.style.setProperty('--accent-500-15', `rgba(${r}, ${g}, ${b}, 0.15)`);
  root.style.setProperty('--accent-500-20', `rgba(${r}, ${g}, ${b}, 0.2)`);
  root.style.setProperty('--accent-500-25', `rgba(${r}, ${g}, ${b}, 0.25)`);
  root.style.setProperty('--accent-500-30', `rgba(${r}, ${g}, ${b}, 0.3)`);
  root.style.setProperty('--accent-500-50', `rgba(${r}, ${g}, ${b}, 0.5)`);
}

export function removeCustomAccentVars() {
  if (typeof document === 'undefined') return;
  const root = document.documentElement;
  [
    '--accent-500',
    '--accent-400',
    '--accent-600',
    '--accent-500-10',
    '--accent-500-15',
    '--accent-500-20',
    '--accent-500-25',
    '--accent-500-30',
    '--accent-500-50',
  ].forEach((k) => root.style.removeProperty(k));
}

